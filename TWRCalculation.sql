WITH DailyReturns AS (
    SELECT 1 AS Day, 0.0 AS DailyReturn
    UNION ALL SELECT 2, 0.0
    UNION ALL SELECT 3, -0.006253657
    UNION ALL SELECT 4, -0.00361612
    UNION ALL SELECT 5, -0.004777986
    UNION ALL SELECT 6, -0.026842209
    UNION ALL SELECT 7, 0.000095331
    UNION ALL SELECT 8, 0.0
    UNION ALL SELECT 9, 0.0
    UNION ALL SELECT 10, -0.004793408
)
SELECT 
    EXP(SUM(LOG(1 + DailyReturn))) - 1 AS TWR
FROM DailyReturns;



